/** Compute indicator color for a given pH */
export function getIndicatorColor(indicatorId: string, pH: number): string {
  if (indicatorId === 'phenolphthalein') {
    if (pH < 8.2) return 'rgba(255,255,255,0)'; // colorless
    if (pH < 10) return `rgba(255,80,180,${(pH - 8.2) / 1.8 * 0.85})`;
    return 'rgba(255,80,180,0.85)';
  }
  if (indicatorId === 'methyl_orange') {
    if (pH < 3.1) return 'rgba(220,40,40,0.8)';
    if (pH < 4.4) return `rgba(255,140,0,0.8)`;
    return 'rgba(220,200,0,0.7)';
  }
  if (indicatorId === 'universal_indicator') {
    // approximate rainbow mapping pH 1-14
    const colors: [number, string][] = [
      [1,  'rgba(220,20,20,0.85)'],
      [3,  'rgba(220,80,20,0.8)'],
      [5,  'rgba(220,180,20,0.75)'],
      [7,  'rgba(20,180,20,0.7)'],
      [9,  'rgba(20,180,200,0.75)'],
      [11, 'rgba(40,60,220,0.75)'],
      [14, 'rgba(100,20,220,0.75)'],
    ];
    for (let i = 0; i < colors.length - 1; i++) {
      if (pH <= colors[i + 1][0]) return colors[i][1];
    }
    return colors[colors.length - 1][1];
  }
  return 'rgba(255,255,255,0)';
}

/** Compute mixture color from its dissolved components */
export function computeMixtureColor(
  components: { chemicalId: string; moles: number }[],
  totalVolumeML: number,
  pH: number,
): string {
  const indicators = ['phenolphthalein', 'methyl_orange', 'universal_indicator'];
  // If indicator present, its color dominates when colored
  for (const ind of indicators) {
    const c = components.find((c) => c.chemicalId === ind);
    if (c && c.moles > 1e-8) {
      const col = getIndicatorColor(ind, pH);
      if (col !== 'rgba(255,255,255,0)') return col;
    }
  }
  // Copper sulfate blue is distinctive
  const cu = components.find((c) => c.chemicalId === 'CuSO4');
  if (cu && cu.moles > 1e-6) {
    const conc = cu.moles / (totalVolumeML / 1000);
    const alpha = Math.min(conc * 5, 0.9);
    return `rgba(0,130,210,${alpha.toFixed(2)})`;
  }
  // KMnO4 purple
  const km = components.find((c) => c.chemicalId === 'KMnO4');
  if (km && km.moles > 1e-8) {
    const conc = km.moles / (totalVolumeML / 1000);
    const alpha = Math.min(conc * 50, 0.9);
    return `rgba(140,20,180,${alpha.toFixed(2)})`;
  }
  return 'rgba(160,210,255,0.28)'; // default: colorless aqueous
}
